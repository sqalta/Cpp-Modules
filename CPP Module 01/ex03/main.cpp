#include "humanA.hpp"
#include "humanB.hpp"
#include "weapon.hpp"

using std::cout;
using std::endl;


int main()
{
	{
		Weapon club = Weapon("crude spiked club");

		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	{
		Weapon club = Weapon("crude spiked club");

		HumanB jim("jim");
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}
	system("leaks weapon");
	return (0);
}
