#include "zombie.hpp"

Zombie::Zombie()
{
	this->name = "";
	std::cout << "Unnamed zombie is created" << std::endl;
}

Zombie::Zombie(std::string name)
{
	this->name = name;
}

Zombie::~Zombie()
{
	std::cout << this->name << ": is died" << std::endl;
}

void Zombie::announce(void)
{
	std::cout << this->name << ": BraiiiiiiinnnzzzZ..." << std::endl;
}
